#Add a New Key-Value Pair
#Problem:
#Given the dictionary: person = {'name': 'Bob', 'age': 30, 'city': 'San Francisco'}

person = {'name': 'Bob', 'age': 30, 
                 'city': 'San Francisco'}
person["country"]="USA"
print(person)

#output
{'name': 'Bob', 'age': 30, 'city': 'San Francisco', 'country': 'USA'}
